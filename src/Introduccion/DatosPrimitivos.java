package Introduccion;

/**
 * Datos primitivos en Java son 8
 * 4 tipos de datos de forma enteras
 * 2 tipos de datos de forma flotante
 * 1 tipo de datos de tipo char
 * 1 tipo de dato de tipo booleano
 */
public class DatosPrimitivos {

    public static void main(String[] args) {
        // Tipo de datos enteros
        byte numeroByte = 1;
        System.out.println("numeroByte = " + numeroByte);
        short numeroShort = 230;
        System.out.println("numeroShort = " + numeroShort);
        int numeroInt = 4502;
        System.out.println("numeroInt = " + numeroInt);
        long numeroLong = 48200;
        System.out.println("numeroLong = " + numeroLong);
        
        // Tipo de dato flotante
        float numeroFloat = 45.0f;
        System.out.println("numeroFloat = " + numeroFloat);
        double numeroDouble = 45.06523d;
        System.out.println("numeroDouble = " + numeroDouble);
        
        // Tipo de dato char
        char caracter = 'a';
        System.out.println("caracter = " + caracter);
        
        // Tipo de dato boleano
        boolean isTrue = false;
        System.out.println("isTrue = " + isTrue);
        
    }
}
