package Operadores;

public class ClaseMath {
    public static void main(String[] args) {
        double raizCuadrada = Math.sqrt(46);
        System.out.println("raizCuadrada = " + raizCuadrada);
        double potencia = Math.pow(4, 3);
        System.out.println("potencia = " + potencia);
        double redondear = Math.round(raizCuadrada);
        System.out.println("redondear = " + redondear);
    }
}
